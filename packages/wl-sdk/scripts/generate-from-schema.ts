const DML_SHARED_HELPERS = `
/**
 * Format a string array as a JSON array SQL literal, mirroring Go's
 * \`formatTagsJSON\` (commons.go:854). Empty/undefined arrays return the
 * literal \`NULL\`. Backslashes and double quotes inside tags are escaped.
 */
export function formatTagsJson(tags: readonly string[] | undefined): string {
  if (!tags || tags.length === 0) return 'NULL';
  const escaped = tags.map(t => t.replace(/\\\\/g, '\\\\\\\\').replace(/"/g, '\\\\"'));
  const jsonStr = \`["\${escaped.join('","')}"]\`;
  return \`'\${jsonStr.replace(/'/g, "''")}'\`;
}

/**
 * Format a Date as the UTC string \`YYYY-MM-DD HH:MM:SS\` used by the Go
 * reference for \`created_at\` / \`updated_at\` literals.
 */
export function formatNowUtc(d: Date): string {
  const pad = (n: number): string => n.toString().padStart(2, '0');
  return (
    \`\${d.getUTCFullYear()}-\${pad(d.getUTCMonth() + 1)}-\${pad(d.getUTCDate())} \` +
    \`\${pad(d.getUTCHours())}:\${pad(d.getUTCMinutes())}:\${pad(d.getUTCSeconds())}\`
  );
}
`.trim();

function emitDmlFile(): string {
  const sections: string[] = [];
  sections.push(HEADER_LINES.join('\n'));
  sections.push(`import { escapeSqlString, sqlStringOrNull } from './escape';`);
  sections.push(DML_SHARED_HELPERS);
  for (const helper of DML_HELPERS) {
    sections.push(`// ${helper.goRef}`);
    sections.push(helper.source);
  }
  return sections.join('\n\n') + '\n';
}

