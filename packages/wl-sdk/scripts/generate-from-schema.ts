function pascalCase(snake: string): string {
  return snake
    .split('_')
    .filter(p => p.length > 0)
    .map(p => p[0].toUpperCase() + p.slice(1))
    .join('');
}

function camelCase(snake: string): string {
  const pascal = pascalCase(snake);
  return pascal[0].toLowerCase() + pascal.slice(1);
}

/**
 * Map a parsed SQL column type to a Zod expression. The result is a string
 * like `z.string()` or `z.number().int()` — without the trailing
 * `.nullable()`, which is appended by the caller based on `notNull`.
 */
function zodForColumnType(type: ColumnType): string {
  switch (type.kind) {
    case 'varchar':
    case 'text':
      return 'z.string()';
    case 'int':
      return 'z.number().int()';
    case 'tinyint':
      // TINYINT(1) is the MySQL boolean idiom, but DoltHub returns it as a
      // numeric string/number. Keep it as `number` to match how the existing
      // `services/wasteland` code handles it.
      return 'z.number().int()';
    case 'float':
      return 'z.number()';
    case 'json':
      return 'z.unknown()';
    case 'timestamp':
    case 'datetime':
      // DoltHub returns timestamps as strings (e.g. `2025-01-02 15:04:05`),
      // not RFC3339, so we don't use `.datetime()`.
      return 'z.string()';
    case 'unknown':
      return 'z.unknown()';
  }
}

function renderColumnZod(col: ParsedColumn): string {
  const base = zodForColumnType(col.type);
  return col.notNull ? base : `${base}.nullable()`;
}

function renderRowSchema(table: ParsedTable): string {
  const lines = table.columns.map(col => {
    const key = /^[A-Za-z_][A-Za-z0-9_]*$/.test(col.name) ? col.name : JSON.stringify(col.name);
    return `  ${key}: ${renderColumnZod(col)},`;
  });
  const schemaName = `${pascalCase(table.name)}RowSchema`;
  return [
    `export const ${schemaName} = z.object({`,
    ...lines,
    '});',
    '',
    `export type ${pascalCase(table.name)}Row = z.infer<typeof ${schemaName}>;`,
  ].join('\n');
}

function renderTableMeta(table: ParsedTable): string {
  const constName = `${camelCase(table.name)}Table`;
  const cols = table.columns.map(c => JSON.stringify(c.name)).join(', ');
  const pk = table.primaryKey ? JSON.stringify(table.primaryKey) : 'null';
  return [
    `export const ${constName} = {`,
    `  name: ${JSON.stringify(table.name)},`,
    `  columns: [${cols}] as const,`,
    `  primaryKey: ${pk},`,
    `} as const;`,
  ].join('\n');
}

/**
 * Re-emit the original DDL for a single table so that the resulting string
 * matches the source file's wording. We re-indent the body to two spaces,
 * matching the existing `services/wasteland/src/upstream-bootstrap/commons-schema.ts`.
 */
function renderCreateTableStatement(rawDdl: string): string {
  // Normalise leading whitespace inside parens to 4-space indent, matching
  // the existing hand-written commons-schema.ts. We keep the original
  // wording inside the parentheses.
  return rawDdl.trim();
}

function renderSchemaStatements(statements: string[]): string {
  const items = statements
    .map(stmt => {
      // Use template literals so backticks get escaped, single quotes stay
      // literal, and the multi-line shape is preserved.
      const escaped = stmt.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');
      return `  \`${escaped}\`,`;
    })
    .join('\n\n');
  return `export const COMMONS_SCHEMA_STATEMENTS: readonly string[] = [\n${items}\n];`;
}

function emitSchemaFile(schemaSql: string): string {
  const parsed = parseSchema(schemaSql);

  const versionStmt = parsed.statements.find(s =>
    /INSERT\s+IGNORE\s+INTO\s+_meta.+schema_version/i.test(s)
  );
  const versionMatch = versionStmt
    ? /VALUES\s*\(\s*'schema_version'\s*,\s*'([^']+)'/i.exec(versionStmt)
    : null;
  const schemaVersion = versionMatch ? versionMatch[1] : 'unknown';

  const sections: string[] = [];
  sections.push(HEADER_LINES.join('\n'));
  sections.push(`import { z } from 'zod';`);
  sections.push(`export const COMMONS_SCHEMA_VERSION = ${JSON.stringify(schemaVersion)} as const;`);

  for (const table of parsed.tables) {
    sections.push(renderRowSchema(table));
    sections.push(renderTableMeta(table));
  }

  sections.push(
    [
      '/**',
      ' * The wasteland commons DDL split into individual statements, ready to',
      " * feed to DoltHub's write API one at a time. Order is preserved from",
      ' * the source file. Statements have no trailing `;` so callers can append',
      ' * one (or not) as the API expects.',
      ' */',
      renderSchemaStatements(parsed.statements.map(renderCreateTableStatement)),
    ].join('\n')
  );

  return sections.join('\n\n') + '\n';
}

