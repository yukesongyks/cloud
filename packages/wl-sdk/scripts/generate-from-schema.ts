/**
 * Schema generator for `@kilocode/wl-sdk`.
 *
 * Reads `wasteland/schema/commons.sql` (the canonical wasteland schema) and
 * emits two TypeScript files into `src/commons/`:
 *
 *  - `schema.generated.ts` — Zod row schemas, row type aliases, table column
 *    metadata, and `COMMONS_SCHEMA_STATEMENTS` (the DDL split into individual
 *    statements, suitable for feeding into DoltHub's write API one at a time).
 *
 *  - `dml.generated.ts` — TypeScript ports of each `*DML` helper in the Go
 *    reference at `wasteland/internal/commons/commons.go`. These are NOT
 *    derived from the SQL schema; they are hand-written templates owned by
 *    this generator. When a new Go helper is added, update `DML_HELPERS`
 *    below and regenerate. Each emitted TS function produces byte-for-byte
 *    identical SQL to its Go counterpart for the same inputs.
 *
 * After writing both files, the generator shells out to `pnpm format` so the
 * output is oxfmt-clean and CI's drift check stays stable.
 *
 * Path resolution: `commons.sql` is read from
 * `${WASTELAND_REPO_PATH}/schema/commons.sql`, where `WASTELAND_REPO_PATH`
 * defaults to `../wasteland` relative to the cloud repo root. Override the
 * env var when the wasteland repo lives elsewhere.
 *
 * To regenerate manually: `pnpm --filter @kilocode/wl-sdk generate`
 */

